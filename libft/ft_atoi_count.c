int		ft_atoi_count(char *str, int *k)
{
	int		neg;
	int		ret;

	neg = 1;
	ret = 0;
	while(str[*k] >= 0 && str[*k] <= 32)
		(*k)++;
	if (str[*k] == '-')
		neg = -1;
	if (str[*k] == '+' || str[*k] == '-')
		(*k)++;
	while (str[*k] && str[*k] >= '0' && str[*k] <= '9')
	{
		ret *= 10;
		ret += str[*k] - '0';
		(*k)++;
	}
	return (neg * ret);
}
